const sent = ref(false)

async function sendMessage() {
  isLoading.value = true;
  sent.value = false;
  await emailjs.send('service_v_portfolio', 'v_portfolio_temp', formBody)
  resetForm();
  sent.value = true;
  isLoading.value = false;
  setTimeout(() => {
    sent.value = false
  }, 3000)
}

function resetForm() {
  for (let field in formBody) {
    formBody[field] = ""
  }
}
